_GENERIC_BLOCK_BODIES = [
    r"Access Denied",
    r"Forbidden",
    r"Request Blocked",
    r"Security violation",
    r"Illegal request",
    r"Attack detected",
    # Inline WAF responses that return 200 OK with a block message in the body
    r"WAF:\s*BLOCKED",
    r"WAF caught",
]

# SQLi probe — triggers most SQL-aware WAFs
_PROBE_PAYLOAD = "' OR '1'='1\"-- -"


@dataclass
class WafResult:
    detected:   bool
    name:       Optional[str]
    evasions:   List[str]
    confidence: str  # "high" | "medium" | "low" | "none"


def detect(injector, url: str, param: Optional[str] = None) -> WafResult:
    """Fire a probe request and fingerprint the WAF from the response."""
    try:
        if param:
            probe_url = _inject_param(url, param, _PROBE_PAYLOAD)
        else:
            sep = "&" if "?" in url else "?"
            probe_url = f"{url}{sep}q={up.quote(_PROBE_PAYLOAD)}"

        resp = injector.get(probe_url)
    except Exception:
        return WafResult(detected=False, name=None, evasions=[EVASION_NONE], confidence="none")

    headers_lower: Dict[str, str] = {k.lower(): v.lower() for k, v in resp.headers.items()}
    body = resp.text

    best_match: Optional[WafSignature] = None
    best_score = 0

    for sig in _SIGNATURES:
        score = 0
        for hdr, pattern in sig.header_clues:
            val = headers_lower.get(hdr, "")
            if val and re.search(pattern, val, re.IGNORECASE):
                score += 2
        for pattern in sig.body_clues:
            if re.search(pattern, body, re.IGNORECASE):
                score += 1
        if resp.status_code in sig.status_codes:
            score += 1
        if score > best_score:
            best_score = score
            best_match = sig

    if best_match and best_score >= 2:
        confidence = "high" if best_score >= 4 else "medium"
        return WafResult(
            detected=True,
            name=best_match.name,
            evasions=best_match.evasions,
            confidence=confidence,
        )

    _body_blocked = any(re.search(p, body, re.IGNORECASE) for p in _GENERIC_BLOCK_BODIES)
    if resp.status_code in (403, 406, 429, 503) or _body_blocked:
        if _body_blocked and resp.status_code < 400:
            return WafResult(
                detected=True,
                name="Generic WAF (inline body block — double-encode bypass likely)",
                evasions=[EVASION_SQL_COMMENT, EVASION_SQL_CASE, EVASION_DOUBLE_ENCODE],
                confidence="medium",
            )
        # Hard block (4xx): probe with double-encoded payload — if it bypasses,
        # the WAF inspects the raw URL before decoding.
        try:
            double_enc_payload = up.quote(up.quote(_PROBE_PAYLOAD, safe=""), safe="")
            if param:
                de_url = _inject_param(url, param, double_enc_payload)
            else:
                sep = "&" if "?" in url else "?"
                de_url = f"{url}{sep}q={double_enc_payload}"
            de_resp = injector.get(de_url)
            _de_body_blocked = any(re.search(p, de_resp.text, re.IGNORECASE) for p in _GENERIC_BLOCK_BODIES)
            if de_resp.status_code < 400 and not _de_body_blocked:
                return WafResult(
                    detected=True,
                    name="Generic WAF (double-encode bypass)",
                    evasions=[EVASION_DOUBLE_ENCODE, EVASION_SQL_COMMENT, EVASION_SQL_CASE],
                    confidence="medium",
                )
        except Exception:
            pass
        return WafResult(
            detected=True,
            name="Generic WAF",
            evasions=[EVASION_SQL_COMMENT, EVASION_SQL_CASE, EVASION_CASE_MIXING, EVASION_HTML_ENCODE],
            confidence="low",
        )

    return WafResult(detected=False, name=None, evasions=[EVASION_NONE], confidence="none")


def _inject_param(url: str, param: str, value: str) -> str:
    parsed = up.urlparse(url)
    qs = up.parse_qs(parsed.query, keep_blank_values=True)
    qs[param] = [value]
    new_qs = up.urlencode(qs, doseq=True)
    return up.urlunparse(parsed._replace(query=new_qs))
